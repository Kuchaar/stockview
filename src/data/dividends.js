const today = () => new Date().toISOString().split('T')[0];

export function getUpcomingDividends() {
  const t = today();
  return dividendEvents
    .filter((d) => d.exDate >= t)
    .sort((a, b) => a.exDate.localeCompare(b.exDate));
}

export function getRecentDividends() {
  const t = today();
  const d90 = new Date();
  d90.setDate(d90.getDate() - 90);
  const cutoff = d90.toISOString().split('T')[0];
  return dividendEvents
    .filter((d) => d.exDate < t && d.exDate >= cutoff)
    .sort((a, b) => b.exDate.localeCompare(a.exDate));
}

export function getDividendsByStatus(status) {
  return dividendEvents.filter((d) => d.status === status);
}
